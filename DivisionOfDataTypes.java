package BasicOfDSA;

public class DivisionOfDataTypes {
    public static void main(String[] args) {
        int a=2;
        double b=4;
        System.out.println(b/a);
        System.out.println(a/b);
        double x = 5/2;// int divided by int gives int
        System.out.println(x);
        x= 5.0/2;
        System.out.println(x);
        x=5.0/2.0;
        System.out.println(x);
        x=2*5;
        System.out.println(x);
        x=2.2*5.0;
        System.out.println(x);
    }
}
